#include <stdio.h>

/**
 * @brief      Converts Celsius to Fahrenheit
 *
 * @param[in]  argc  The argument count
 * @param      argv  The argument vector
 *
 * @return     0 on success
 */
int main(int argc, char const *argv[])
{
  float C, F;
  printf("Enter the Celsius: ");
  scanf("%f", &C);

  F = C * 9 / 5 + 32;
  printf("The Fahrenheit of %f is %f", C, F);

  return 0;
}
